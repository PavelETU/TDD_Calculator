public class CalculationBase {

    public static double add(double f, double s) {
        return f + s;
    }

    public static double subtract(double f, double s) {
        return f - s;
    }

    public static double multiply(double f, double s) {
        return f * s;
    }

    public static double divide(double f, double s) {
        return f / s;
    }

}
